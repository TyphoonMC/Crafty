// glyphRows returns the bitmap for r, falling back to a solid block for
// unknown runes so the user can still see that something was typed.
func glyphRows(r rune) [7]uint8 {
	if g, ok := terminalFont[r]; ok {
		return g
	}
	return [7]uint8{0b11111, 0b11111, 0b11111, 0b11111, 0b11111, 0b11111, 0b11111}
}
