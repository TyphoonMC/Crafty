package main

import "github.com/go-gl/gl/v2.1/gl"

func drawCube() {
	gl.Color4f(1, 1, 1, 1)

	gl.Begin(gl.QUADS)

	gl.Normal3f(0, 0, 1)
	gl.TexCoord2f(0, 0)
	gl.Vertex3f(0, 0, 1)
	gl.TexCoord2f(1, 0)
	gl.Vertex3f(1, 0, 1)
	gl.TexCoord2f(1, 1)
	gl.Vertex3f(1, 1, 1)
	gl.TexCoord2f(0, 1)
	gl.Vertex3f(0, 1, 1)

	gl.Normal3f(0, 0, 0)
	gl.TexCoord2f(1, 0)
	gl.Vertex3f(0, 0, 0)
	gl.TexCoord2f(1, 1)
	gl.Vertex3f(0, 1, 0)
	gl.TexCoord2f(0, 1)
	gl.Vertex3f(1, 1, 0)
	gl.TexCoord2f(0, 0)
	gl.Vertex3f(1, 0, 0)

	gl.Normal3f(0, 1, 0)
	gl.TexCoord2f(0, 1)
	gl.Vertex3f(0, 1, 0)
	gl.TexCoord2f(0, 0)
	gl.Vertex3f(0, 1, 1)
	gl.TexCoord2f(1, 0)
	gl.Vertex3f(1, 1, 1)
	gl.TexCoord2f(1, 1)
	gl.Vertex3f(1, 1, 0)

	gl.Normal3f(0, 0, 0)
	gl.TexCoord2f(1, 1)
	gl.Vertex3f(0, 0, 0)
	gl.TexCoord2f(0, 1)
	gl.Vertex3f(1, 0, 0)
	gl.TexCoord2f(0, 0)
	gl.Vertex3f(1, 0, 1)
	gl.TexCoord2f(1, 0)
	gl.Vertex3f(0, 0, 1)

	gl.Normal3f(1, 0, 0)
	gl.TexCoord2f(1, 0)
	gl.Vertex3f(1, 0, 0)
	gl.TexCoord2f(1, 1)
	gl.Vertex3f(1, 1, 0)
	gl.TexCoord2f(0, 1)
	gl.Vertex3f(1, 1, 1)
	gl.TexCoord2f(0, 0)
	gl.Vertex3f(1, 0, 1)

	gl.Normal3f(0, 0, 0)
	gl.TexCoord2f(0, 0)
	gl.Vertex3f(0, 0, 0)
	gl.TexCoord2f(1, 0)
	gl.Vertex3f(0, 0, 1)
	gl.TexCoord2f(1, 1)
	gl.Vertex3f(0, 1, 1)
	gl.TexCoord2f(0, 1)
	gl.Vertex3f(0, 1, 0)

	gl.End()
}

